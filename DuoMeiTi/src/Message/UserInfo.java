package Message;

public class UserInfo{
	public String username;
	public int id;
	UserInfo(){}
	UserInfo(String username,Integer id){
		this.username=username;
		this.id=id.intValue();
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
}
